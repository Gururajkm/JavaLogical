class PositiveNegative {
    public static void main(String[] args) {
        System.out.println("Main started");
        int n = -5;
        if (n < 0) {
            System.out.println(n + " is  a Negative number");
        } else {
            System.out.println(n + " is a Positive number ");
        }
        System.out.println("Main ended");
    }

}
