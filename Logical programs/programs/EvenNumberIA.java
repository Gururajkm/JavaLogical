
class EvenNumberIA {
    public static void main(String[] args) {
        int i;
        int[] a = { 10, 20, 30, 40, 4, 3, 2, 8, 9 };
        for (i = 0; i < a.length; i++) {
            if ((a[i] % 2) == 0) {
                System.out.println("The Even numbers in the array are " + a[i]);
            }

        }

    }

}
