class EvenOdd {
    public static void main(String[] args) {
        System.out.println(" Main  is Strated");
        int n = 9;
        if (n % 2 == 0) {
            System.out.println(n + " is a Even Number");
        } else {
            System.out.println(n + " is a Odd Number");
        }
        System.out.println(" Main is Ended");
    }

}
