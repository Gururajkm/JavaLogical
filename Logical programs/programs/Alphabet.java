
class Alphabet {
    public static void main(String[] args) {
        char ch = 'g';
        if ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z')) {
            System.out.println("Given Character is Alphabet");
        } else {
            System.out.println("Given character is not Alphabet");
        }

    }
}
